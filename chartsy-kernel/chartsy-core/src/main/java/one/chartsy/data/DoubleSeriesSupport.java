package one.chartsy.data;

public class DoubleSeriesSupport {

    public static double multiply(double x, double y) {
        return x * y;
    }

    public static double divide(double x, double y) {
        return x / y;
    }

    public static double subtract(double x, double y) {
        return x - y;
    }
}
